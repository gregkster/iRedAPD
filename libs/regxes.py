import re

# Mail address.
#
#   - `+`, `=` are used in SRS rewritten addresses.
#   - `/` is sub-folder. e.g. 'john+lists/abc/def@domain.com' will create
#     directory `lists` and its sub-folders `lists/abc/`, `lists/abc/def`.
#   - `~` is a legal char according to RFC document.
#
# Although many special characters are allowed in username part of email
# address, but that doesn't mean it's a good idea/choice, please try to use
# only letters `[a-z]` and `.-_` in username.
regx_email = r'''[\w\-\#\.\+\=\/\&\~]*@[\w\-][\w\-\.]*\.[a-zA-Z0-9\-]{2,15}'''
cmp_email = re.compile(regx_email, re.IGNORECASE | re.DOTALL)

regx_email_srs = r'''[\w\!\#\$\%\&\'\*\+\-\/\=\?\^\_\`\{\|\}\~\"]*@[\w\-][\w\-\.]*\.[a-zA-Z0-9\-]{2,15}'''
cmp_email_srs = re.compile(regx_email_srs, re.IGNORECASE | re.DOTALL)

# Domain name
regx_domain = r'''[\w\-][\w\-\.]*\.[a-z]{2,15}'''
cmp_domain = re.compile(regx_domain, re.IGNORECASE | re.DOTALL)

regx_top_level_domain = r'''[a-z0-9\-]{2,25}'''
cmp_top_level_domain = re.compile(regx_top_level_domain, re.IGNORECASE | re.DOTALL)

# IP address
